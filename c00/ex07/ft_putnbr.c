#include<unistd.h>

void	print_c(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		print_c('-');
		nb = -nb;
	}
	if (nb < 10)
	{
		print_c('0' + nb);
		return ;
	}
	ft_putnbr(nb / 10);
	print_c('0' + (nb % 10));
}
