#include<unistd.h>

int	ft_sqrt(int nb)
{
	long	i;
	long	a;

	i = 2;
	a = nb;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	i = 2;
	while (i * i <= a)
	{
		if (i * i == a)
			return (i);
		i++;
	}
	return (0);
}
